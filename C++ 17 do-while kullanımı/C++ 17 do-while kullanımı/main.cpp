// C++ DİLİNDE DO-WHİLE KULLANIMI

// bu da diğerleri gibi kullanımı c dilindekine benzerdir
// while da çok benzerdir fakat ufak bir fark var
// bu şarta bakmaksızın bir kez çalışır sonra şarta bakar
// kullanımı şu şekildedir :
// do {ifadeler} while(şart);


#include <iostream>

int main() {
    
    //şart false olduğu halde bir kez çalıştı
    do
    {
        std::cout<<"bilinmeyen işlem"<<'\n';
    } while(false);
    
    
    
}
